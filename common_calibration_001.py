# Threshold:
#   Sensoer ID : SensID
#   Buffer Size: Size
#   T1: R1
#   T2: R2
#   T3: a3, b3
#   T4: B4
#   T5: a5, b5
#   T6: a6, b6
#   T7: a7, b7
#   Label: Label

# Threshold Param:
#   LL: Lower Left  (左下)
#   UR: Upper Right (右上)
#
#   Sensoer ID : SensID
#   Buffer Size: Size
#   T1: T1R
#   T2: T2R
#   T3: ((LL3G, LL3R), (UR3G, UR3R)) : usually LL3R = 0, UR3R = T2R
#   T4: T4B
#   T5: ((LL5B, LL5G), (UR5B, UR5G)) : usually LL5B = 0, UR5B = T4B
#   T6: ((LL6B, LL6G), (UR6B, UR6G)) : usually LL6G = 0
#   T7: ((LL7B, LL7G), (UR7B, UR7G)) : usually LL7G = 0
#   Label: Label

# Data Structure of Threshold Param:
#   (  SensID, Size,
#      T1R,
#      T2R,
#    (LL3G, (UR3G, UR3R)),
#      T4B,
#    (LL5G, (UR5B, UR5G)),
#    (LL6B, (UR6B, UR6G)),
#    (LL7B, (UR7B, UR7G)),
#      Label )

class DataClass: pass

def ThParamTupleToClass(Tup):
    P = DataClass()
    ( P.SensID, P.Size,   P.T1R,  P.T2R,
      (P.LL3G, (P.UR3G, P.UR3R)),
      P.T4B,
      (P.LL5G, (P.UR5B, P.UR5G)),
      (P.LL6B, (P.UR6B, P.UR6G)),
      (P.LL7B, (P.UR7B, P.UR7G)),
      P.Label ) = Tup
    P.LL3R = 0.0
    P.LL5B = 0.0
    P.LL6G = 0.0
    P.LL7G = 0.0
    return P

def TwoPointsToLine(Sx, Sy, Tx, Ty):
    a = (Ty-Sy)/(Tx-Sx)
    b = Ty-a*Tx
    return a, b

def ThParamClassToThClass(P):
    T = DataClass()
    T.SensID   = P.SensID
    T.Size     = P.Size
    T.Buffer   = ['' for i in range(T.Size)]
    T.R1       = P.T1R
    T.R2       = P.T2R
    T.a3, T.b3 = TwoPointsToLine(P.LL3G, P.LL3R, P.UR3G, P.UR3R)
    T.B4       = P.T4B
    T.a5, T.b5 = TwoPointsToLine(P.LL5B, P.LL5G, P.UR5B, P.UR5G)
    T.a6, T.b6 = TwoPointsToLine(P.LL6B, P.LL6G, P.UR6B, P.UR6G)
    T.a7, T.b7 = TwoPointsToLine(P.LL7B, P.LL7G, P.UR7B, P.UR7G)
    T.Label    = P.Label
    return T

def CleanBuffer(T):
    T.Buffer = ['' for i in range(T.Size)]

def instDecisionArea(T, R, G, B):
    area = ''
    if R>=T.R1:
        if B>=T.B4:
            area = 'White1'
        elif G>=(T.a5*B+T.b5):
            area = Color.YELLOW
        else:
            area = Color.RED
    elif R>=T.R2:
        area = 'White2'
    else:
        if R>=(T.a3*G+T.b3):
            if G>=(T.a6*B+T.b6):
                area = Color.BLACK
            else:
                area = 'Blue1'
        else:
            if G>=(T.a7*B+T.b7):
                area = Color.GREEN
            else:
                area = 'Blue2'
    return area



class MyColorSensor:
    ID           = 0
    SaveRGB      = False
    UseSensor    = True
    alt_RGB_path = '010Black.csv'
    @classmethod
    def setup(cls):
        cls.TH = ThParamClassToThClass(ThParamTupleToClass(THPARAM[cls.ID]))

    @classmethod
    def open(cls):
        if not cls.UseSensor:
            cls.altRGBfile = open(cls.alt_RGB_path, 'r')
            cls.altRGBfile.readline()

        if cls.SaveRGB:
            SensID = '{:0>3}'.format(str(cls.ID))
            FileID_path = 'RGB_' + SensID + '_FileID.txt'
            try:
                file = open(FileID_path, 'r')
                s = file.readline()
                s = s.rstrip('\n')
                FileID = '{:0>4}'.format(str(int(s)+1))
                file.close()
            except:
                FileID = '0001'
            file = open(FileID_path, 'w')
            file.write(FileID)
            file.close()
            RGB_path    = 'RGB_' + SensID + '_' + FileID + '.csv'
            cls.RGBfile = open(RGB_path, 'w')
            s = '# R, G, B, '
            for n in range(cls.TH.Size):
                s += 'inst D(' + str(n) + '), '
            s += 'final D'
            cls.RGBfile.write(s)


    @classmethod
    def getRGB(cls, colorsensor):
        if cls.UseSensor:
            cls.R, cls.G, cls.B = colorsensor.rgb()
        else:
            s = cls.altRGBfile.readline()
            if s == '':
                cls.close()
                raise EOFError('\n\nIgnore this Error.\nJust the file [' + cls.alt_RGB_path + '] reaches to EOF.\n')
            else:
                s = s.rstrip('\n')
                R, G, B = s.split(",")[0:3]
                cls.R = float(R)
                cls.G = float(G)
                cls.B = float(B)
        return True

    @classmethod
    def instDecision(cls):
        area = instDecisionArea(cls.TH, cls.R, cls.G, cls.B)
        if   area=='White1': color = Color.WHITE
        elif area=='White2': color = Color.WHITE
        elif area=='Blue1':  color = Color.BLUE
        elif area=='Blue2':  color = Color.BLUE
        else:                color = area
        cls.TH.Buffer.append(color)
        del cls.TH.Buffer[0]
        if cls.SaveRGB:
            s = '\n' + str(cls.R) + ',' + str(cls.G) + ',' + str(cls.B) + ','
            for decision in reversed(cls.TH.Buffer):
                s += str(decision) + ','
            #s += '\n'
            cls.RGBfile.write(s)

        return color

    @classmethod
    def finalDecision(cls):
        if   Color.WHITE in cls.TH.Buffer: color = Color.WHITE
        elif Color.BLACK in cls.TH.Buffer: color = Color.BLACK
        else:                              color = cls.TH.Buffer[-1]
        # s = str(cls.R) + ',' + str(cls.G) + ',' + str(cls.B) + ','
        #for decision in reversed(cls.TH.Buffer):
        #    s += str(decision) + ','
        if cls.SaveRGB:
        	s = str(color)
        	cls.RGBfile.write(s)
        return color


    @classmethod
    def close(cls):
        if not cls.UseSensor:
            cls.altRGBfile.close()
        if cls.SaveRGB:
            cls.RGBfile.close()


FirstCall = True
def tape_color(colorsensor):
    global FirstCall
    if FirstCall:
        MyColorSensor.setup()
        MyColorSensor.open()
        FirstCall = False

    MyColorSensor.TH.Buffer   = ['' for i in range(MyColorSensor.TH.Size)]
    MyColorSensor.getRGB(colorsensor)
    MyColorSensor.instDecision()
    size = len(MyColorSensor.TH.Buffer)
    for n in range(size-1):
        wait(50)
        MyColorSensor.getRGB(colorsensor)
        MyColorSensor.instDecision()
    return MyColorSensor.finalDecision()

def finish():
    MyColorSensor.close()

